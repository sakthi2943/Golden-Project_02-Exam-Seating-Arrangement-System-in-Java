    public String checkSeat() {
        String seatNo="";
        try {
            seatNo = AllotSeat();

            String query = "SELECT * FROM seats WHERE seat=?";
            pst = con.prepareStatement(query);
            pst.setString(1, seatNo);

            res = pst.executeQuery();
            
            if(res.next()){
                checkSeat();
            }
        } catch (SQLException ex) {
            Logger.getLogger(ExamSeating.class.getName()).log(Level.SEVERE, null, ex);
        }
        return seatNo;
    }

    public static String AllotSeat() {
        // random	   
        int roomNo = (int)(Math.ceil(Math.random() * 3));
        int seatNo = (int)(Math.ceil(Math.random() * 20));
        //Seating
        String seating = "R" + roomNo + "S" + seatNo;
        return seating;
    }

