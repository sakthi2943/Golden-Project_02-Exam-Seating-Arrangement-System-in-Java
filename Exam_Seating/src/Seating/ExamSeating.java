    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
            String Name = txtName.getText();
            String RollNo = txtRollNo.getText();
            String Department = txtDepartment.getSelectedItem().toString();
            String year = txtYear.getSelectedItem().toString();
            String seatNo = checkSeat();
            try {
                String insertQuery = "INSERT INTO seats (name,rollno,department,year,seat) VALUES (?, ?, ?, ?, ?)";

                pst = con.prepareStatement(insertQuery);

                pst.setString(1, Name);
                pst.setString(2, RollNo);
                pst.setString(3, Department);
                pst.setString(4, year);
                pst.setString(5,seatNo);

                int k = pst.executeUpdate();

                if (k == 1) {
                    JOptionPane.showMessageDialog(this, "Seat Alloted", "Alloted", JOptionPane.PLAIN_MESSAGE);
                    ExamSeat_Load();
                    txtName.setText("");
                    txtRollNo.setText("");
                    txtDepartment.setSelectedItem("Computer Science and Engineering");
                    txtYear.setSelectedItem("I Year");
                    txtName.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(this, "Seat Not Available", "Failed", JOptionPane.ERROR_MESSAGE);
                }
        } catch (SQLException ex) {
            Logger.getLogger(ExamSeating.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

